    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            String sID = studentId.getText();
            String stdName = name.getText();
            String stdStreet = streetName.getText();
            String stdPostcode = postCode.getText();
            String stdCity = city.getText();
            double stdPhone = Double.parseDouble(phone.getText());
            String stdClass = studentClass.getText();

            String sql = "UPDATE student SET stdName = '"+stdName+"', stdPhone = '"+stdPhone+"', stdClass = '"+stdClass+"', street = '"+stdStreet+"', postcode = '"+stdPostcode+"', city = '"+stdCity+"' WHERE id = '"+sID+"'";
            //send update to insert our values
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Student " + stdName + " successfully updated!");
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

