    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            
            String studentName = name.getText();
            String sql = "SELECT * FROM student WHERE stdName = '"+studentName+"'";
            
            rs = stmt.executeQuery(sql);
            
            if(rs.next()) {
                studentId.setText(rs.getString("id"));
                streetName.setText(rs.getString("street"));
                postCode.setText(rs.getString("postcode"));
                phone.setText(rs.getString("stdPhone"));
                city.setText(rs.getString("city"));
                studentClass.setText(rs.getString("stdClass"));
            }else {
                JOptionPane.showMessageDialog(null, "Record not found. Please ensure exact name is typed!");
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

